import static java.lang.Math.*;

public class StaticImportDemo {
    public static void main(String[] args) {
        System.out.println("Square Root of 16: " + sqrt(16));
        System.out.println("Power (2^5): " + pow(2, 5));
        System.out.println("Max of (12, 8): " + max(12, 8));
        System.out.println("Min of (12, 8): " + min(12, 8));
        System.out.println("Absolute of (-25): " + abs(-25));
    }
}
